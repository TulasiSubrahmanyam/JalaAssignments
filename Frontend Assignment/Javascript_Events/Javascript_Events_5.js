// Define the JSON object
const person = {
    "name": "John",
    "age": 30,
    "city": "New York"
  };
  
  // Access properties using dot notation
  console.log("Name:", person.name); // Output: Name: John
  console.log("Age:", person.age); // Output: Age: 30
  console.log("City:", person.city); // Output: City: New York
  