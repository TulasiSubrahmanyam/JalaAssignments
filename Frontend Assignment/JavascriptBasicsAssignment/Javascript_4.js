const person = {
    firstname: "John",
    lastname: "Doe"
  };
  
  // Accessing properties using dot notation
  console.log(person.firstname); // Output: John
  console.log(person.lastname); // Output: Doe
  
  // Accessing properties using bracket notation
  console.log(person['firstname']); // Output: John
  console.log(person['lastname']); // Output: Doe
  